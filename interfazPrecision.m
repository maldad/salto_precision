% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
        axes(handles.axes1);
        hold on;
        cla;
        pa = str2double(get(handles.edit1,'String'));
        pb = str2double(get(handles.edit2,'String'));
        ang = str2double(get(handles.edit3,'String'));
        bas = str2double(get(handles.edit4,'String'));
        tiro(pa, pb, ang, bas)

        num1 = str2double(get(handles.edit1,'String'));
        num2 = str2double(get(handles.edit2,'String'));
        str = int2str(num1+num2);
        set(handles.text7, 'String', str);
        
        porc = str2double(get(handles.edit3,'String'));
        grados = (porc*90)/100;
        str2 = int2str(grados);
        str3 = strcat(str2, '°');
        set(handles.text9, 'String', str3);
        
        vel = double(sqrt(2*9.81*num2));
        set(handles.text11, 'String', strcat(num2str(vel), ' m/s'));
        
        masa = str2double(get(handles.edit5,'String'));
        ec = (masa*(vel^2))/2;
        set(handles.text13, 'String', strcat(num2str(ec), ' J'));
        
        fza = ec*0.001;
        set(handles.text15, 'String', strcat(num2str(fza), ' N'));
